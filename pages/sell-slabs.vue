<script>
import CardListItem from '~/components/dashboard/CardListItem'
export default {
  transition: 'fade',
  layout: 'dashboard',
  head() {
    return {
      title: 'Watch List - Slabstox'
    }
  },
  mounted() {
    this.search()
    this.scroll()
  },
  components: {
    CardListItem
  },
  data() {
    return {
      items: [],
      page: 1,
      requestInProcess: false,
      noMoreData: false
    }
  },
  methods: {
    search(status = false) {
      if (!this.requestInProcess) {
        try {
          if (!status) {
            this.page = 1
            this.items = []
          }
          this.requestInProcess = true
          this.$axios
            .$post('watchlist/search', {
              page: this.page
            })
            .then(res => {
              this.requestInProcess = false
              if (res.status == 200) {
                if (res.data != null && res.data.length > 0) {
                  if (status) {
                    res.data.map(item => {
                      this.items.push(item)
                    })
                  } else {
                    this.items = res.data
                  }
                  this.page = res.next
                } else {
                  if (!status) {
                    this.items = []
                  } else {
                    this.noMoreData = true
                  }
                }
              }
            })
        } catch (err) {
          this.requestInProcess = false
          console.log(err)
        }
      }
    },
    scroll() {
      window.onscroll = () => {
        const scrollTop =
          document.documentElement.scrollTop + window.innerHeight
        const offsetHeight = document.documentElement.offsetHeight
        let bottomOfWindow =
          scrollTop >= offsetHeight - 15 && scrollTop <= offsetHeight + 15
        if (bottomOfWindow) {
          if (!this.noMoreData) {
            this.search(true)
          }
        }
      }
    }
  }
}
</script> 

